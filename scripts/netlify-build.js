/**
 * Netlify build — configures frontend API URL.
 *
 * Set in Netlify → Environment variables (Production):
 *   API_BASE_URL = https://your-api.onrender.com/api   (recommended)
 *   OR API_PROXY_TARGET = https://your-api.onrender.com
 *
 * Note: MONGODB_URI, JWT_SECRET, etc. belong on RENDER (API), not Netlify.
 */
const fs = require('fs');
const path = require('path');

const publicDir = path.join(__dirname, '../public');
const isNetlify = Boolean(process.env.NETLIFY || process.env.NETLIFY_DEV);

function pickBackendUrl() {
  const candidates = [
    process.env.API_BASE_URL,
    process.env.BACKEND_URL,
    process.env.RENDER_EXTERNAL_URL,
    process.env.API_URL,
  ].filter(Boolean);

  for (const raw of candidates) {
    let url = raw.replace(/\/$/, '');
    if (!url.endsWith('/api')) url = `${url}/api`;
    return { mode: 'direct', apiBaseURL: url, source: 'API_BASE_URL' };
  }

  const proxy = process.env.API_PROXY_TARGET?.replace(/\/$/, '');
  if (proxy) {
    return { mode: 'proxy', apiBaseURL: '/api', proxy, source: 'API_PROXY_TARGET' };
  }

  return null;
}

const config = pickBackendUrl();
const redirects = [];

let apiBaseURL = '/api';
let apiConfigured = true;

if (config) {
  apiBaseURL = config.apiBaseURL;
  if (config.mode === 'proxy') {
    redirects.push(`/api/*  ${config.proxy}/api/:splat  200`);
    console.log(`✓ API proxy /api/* → ${config.proxy}/api/*`);
  } else {
    console.log(`✓ Direct API (${config.source}): ${apiBaseURL}`);
  }
} else if (isNetlify) {
  apiConfigured = false;
  apiBaseURL = '';
  console.warn(`
⚠️  API_BASE_URL is not set on Netlify.

   Netlify hosts only the frontend. Your Express API must run on Render/Railway.

   1. Deploy API on Render (npm start) with MONGODB_URI, JWT_SECRET, etc.
   2. In Netlify → Environment variables, ADD:
        API_BASE_URL = https://YOUR-SERVICE.onrender.com/api
   3. Remove MONGODB_URI / JWT_SECRET from Netlify (keep them on Render only).
   4. Redeploy.

   Build continues so the site deploys; login will show a setup message until API_BASE_URL is set.
`);
} else {
  console.log('Local build: API → /api');
}

redirects.push('/*    /index.html   200');

fs.writeFileSync(
  path.join(publicDir, 'js/config.js'),
  `// Generated by npm run build:netlify
window.APP_CONFIG = {
  apiBaseURL: ${JSON.stringify(apiBaseURL)},
  apiConfigured: ${apiConfigured},
};
`
);

fs.writeFileSync(path.join(publicDir, '_redirects'), redirects.join('\n') + '\n');
console.log('Netlify build complete.');
